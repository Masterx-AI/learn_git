# Intialize
git init

# Clone Env
git clone <repo>
git clone --branch <tag> <repo>

# Check status
git status
git log

# File changes
git diff

# Add Files
git add *

# Add commit message
git commit -m "comment"

# Push code changes
git push


# New Branch
git checkout -b <branch_name>

git branch <branch_name>
git switch <branch_name>

# Merge
git merge

# Delete Branch
git branch -d <branch_name>


# Reset
git reset
git revert

git grep <str>